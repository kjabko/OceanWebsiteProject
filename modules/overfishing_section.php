        </div>
		<div class="row featurette">
        <div class="col-md-7">
            <h2>Overcapacity</h2>
			<p class="lead">Worldwide, we have way too many fishing fleets. On a global scale we have enough fishing fleet capacity to
			catch fish from four planets the size of earth.</p>
			<h2>Bycatch discards</h2>
			<p class="lead">Bycatch occurs as modern fishing techniques cover extensive areas and is and highly unselective. The nets in
			addition to catching the target fish also catch many other marine animals as well. These are discarded
			overboard dead or to die.</p>
			<h2>Illegal Fishing</h2>
			<p class="lead">Poor fisheries management in certain countries also contributes to the problem of overfishing. Illegal and
			pirate fishing vessels often ignore the regulations on quotas, net sizes and permitted fishing areas.</p>
			<h2>Bottom trawling</h2>
			<p class="lead">Bottom trawling is the process whereby boats drag large nets along the seabed destroying plant life, coal
			reefs and anything in their path leaving muddy seabeds. These large nets catch young small fish, marine
			turtles, sharks, seabirds, whales, dolphins and porpoises.</p>
